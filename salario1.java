    private void calcular() {
        
        double horasTrabalhadas = Double.parseDouble(jTextField1.getText());
        double valorHora = Double.parseDouble(jTextField13.getText());
        double percentualDesconto = Double.parseDouble(jTextField8.getText());
        
        double salarioBruto = (horasTrabalhadas * valorHora);
        double totalDesconto = ((percentualDesconto/100) * salarioBruto);
        double salarioLiquido = (salarioBruto - totalDesconto);
        
        jLabel8.setText("O total de horas trabalhadas é: " + horasTrabalhadas);
        jLabel5.setText("O salário bruto é: " + salarioBruto);
        jLabel6.setText("O total de desconto é:  " + totalDesconto);
        jLabel7.setText("O salário líquido é: " + salarioLiquido);
    }
